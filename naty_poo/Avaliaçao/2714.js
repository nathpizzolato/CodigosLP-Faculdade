/*Entrada
A primeira linha de entrada é um valor inteiro N (onde, 1 <= N <= 1000) 
que representa a quantidade de RA's que deve ser considerada pelo programa. 
As N seguintes linhas representam, cada uma, strings, de no máximo 100 
caracteres, que representam os RA's enviados pelo sistema de matrícula.
Saída
As saídas válidas para o programa são: a apresentação da senha 
provisória ou a indicação de "INVALID DATA". A senha provisória é 
formada pelos X caracteres númericos não iniciados em 0 
mais a direita da string, por exemplo: para o identificador 
"RA000000000000012340" a senha provisória deve ser "12340". 
Caso a string recebida não esteja de acordo com as regras de formação, 
o programa deve indicar "INVALID DATA".*/